function clampIdade(a) {
  return Math.max(1, Math.min(17, Math.floor(a)));
}

// Índice da coluna de estatura a partir da estatura medida (cm) — a que mais se
// aproxima na tabela, conforme o passo a passo do Manual SBP.
export function colunaPorEstatura(sexo, idadeAnos, cm) {
  const linha = TAB_PA[sexo]?.[clampIdade(idadeAnos)];
  if (!linha || cm == null || isNaN(cm)) return null;
  let melhor = 0;
  let dist = Infinity;
  linha.est.forEach((e, i) => {
    const d = Math.abs(e - cm);
    if (d < dist) { dist = d; melhor = i; }
  });
  return melhor;
}

// Limiares (mmHg) do paciente para um componente ('s' ou 'd')
export function limiaresPA(sexo, idadeAnos, coluna, componente) {
  const linha = TAB_PA[sexo]?.[clampIdade(idadeAnos)];
  if (!linha) return null;
  const c = coluna == null ? COL_P50 : coluna;
  const t = linha[componente];
  return { p50: t.p50[c], p90: t.p90[c], p95: t.p95[c], p95_12: t.p95[c] + 12 };
}

// Faixa de percentil (rótulo) de um valor observado
function faixaPercentil(v, lim) {
  if (v < lim.p50) return "< P50";
  if (v < lim.p90) return "P50–P90";
  if (v < lim.p95) return "P90–P95";
  if (v < lim.p95_12) return "P95–P99";
  return "≥ P95+12";
}

// Estágio de um componente em crianças de 1 a <13 anos (usa a tabela e os
// tetos absolutos de adulto — "o que for menor", Quadro 4).
function estagioCrianca(v, lim, cortes) {
  const limElev = Math.min(lim.p90, cortes.elev);
  const limE1 = Math.min(lim.p95, cortes.e1);
  const limE2 = Math.min(lim.p95_12, cortes.e2);
  if (v >= limE2) return 3;
  if (v >= limE1) return 2;
  if (v >= limElev) return 1;
  return 0;
}

// Estágio de um componente em ≥ 13 anos (critérios de adulto, Quadro 4).
function estagioAdolescente(v, cortes) {
  if (v >= cortes.e2) return 3;
  if (v >= cortes.e1) return 2;
  if (v >= cortes.elev) return 1;
  return 0;
}

// Cortes absolutos de adulto por componente
const CORTES = {
  s: { elev: 120, e1: 130, e2: 140 },
  d: { elev: 80, e1: 80, e2: 90 },
};

// Avaliação completa. Entrada: sexo ('M'/'F'), idadeAnos (decimal), coluna de
// estatura (0–6; null → P50), pas, pad (mmHg). Classificação pelo maior nível
// entre sistólica e diastólica.
export function avaliarPA({ sexo, idadeAnos, coluna, pas, pad }) {
  if (!sexo || idadeAnos == null || isNaN(idadeAnos)) return null;
  if (idadeAnos < 1 || idadeAnos >= 18) return { foraFaixa: true, idadeAnos };

  const temS = pas != null && !isNaN(pas);
  const temD = pad != null && !isNaN(pad);
  if (!temS && !temD) return null;

  const isAdol = idadeAnos >= 13;
  const limS = limiaresPA(sexo, idadeAnos, coluna, "s");
  const limD = limiaresPA(sexo, idadeAnos, coluna, "d");

  const estS = temS
    ? (isAdol ? estagioAdolescente(pas, CORTES.s) : estagioCrianca(pas, limS, CORTES.s))
    : null;
  const estD = temD
    ? (isAdol ? estagioAdolescente(pad, CORTES.d) : estagioCrianca(pad, limD, CORTES.d))
    : null;

  const estagio = Math.max(...[estS, estD].filter((e) => e !== null));

  return {
    foraFaixa: false,
    isAdol,
    estagio,
    estagioS: estS,
    estagioD: estD,
    faixaS: temS ? faixaPercentil(pas, limS) : null,
    faixaD: temD ? faixaPercentil(pad, limD) : null,
    limiares: { s: temS ? limS : null, d: temD ? limD : null },
    colunaUsada: coluna == null ? COL_P50 : coluna,
  };
}

